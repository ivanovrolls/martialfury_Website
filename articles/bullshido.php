    <h2>Picking a new style and school</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
            Those with experience in martial arts will have seen the plethora of fake martial arts
            on the internet; the "martial arts" in these videos are commonly dubbed as "bullshido".
            Businesses that profit on these fraudulent schemes tend to prey on beginners looking to get involved in such sports,
            and exploit misconceptions surrounding martial arts set by modern media.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/sensei_seth.jpg" height = "auto" width = "70%">
        </div>
        <hr>
        <p>
            The intent of this article is to create a guide for picking a martial arts style, choosing a trustworthy school or gym,
            and avoiding fake martial arts scams. For the following topics, I have interviewed Karate <a href = "https://www.youtube.com/channel/UCXYOXWWqgtSrk27402V_JMw">YouTuber Sensei Seth</a>, who has
            done a number of videos on his views about this, and has tons of expereince in coaching, training, and more.
        </p>
        <hr>
        <h3>Picking a martial arts style</h3>
        <p>
            Martial arts are varied. For starters, there is traditional martial arts, and modern martial arts
            (also dubbed combat sports). There are grappling and striking arts, each of which have their own
            strengths and weaknesses. So how exactly does one narrow down the styles that they would like to train in?
            <hr>
            <b stlye = "font-size: 1.302vw">Q: What do you think are some key factors to consider for beginners when picking a martial arts style?</b>
            <hr>
            <i>Sensei Seth: Whichever style it is that you want to do, most likely, there is a free trial class that goes along
              with it. Take it! I suggest sampling different martial arts styles and also thinking about [different schools for
              one style] just because teachers can have [unique teaching styles that suit you better]. 
            </i>
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/mma_instructor.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Sensei Seth goes on to recommend sampling a variety of martial arts styles and schools before making a
            decision. Different teachers have different styles of teaching and could emphasise divergent parts of the style's
            doctrines. This is also good advice when looking for a new school. 
        </p>
        <hr>
        <p>
            Something more to consider might be what your goals are. If you want to learn how to protect yourself,
            then it could be more suitable to lean towards more modern fighting styles as they tend to be easier to apply.
            That's not to say traditional martial arts aren't effective, but they do require a longer amount of
            time and dedication to be used properly. You might just be looking for something to get you into shape, in
            which case combat sports (Boxing, Muay Thai etc.) provide excellent physical conditioning.
        </p>
        <br>
        <h3>Finding a reputable school or gym</h3>
        <p>
            Finding a reliable place to train can be much harder than choosing a fighting style. This is due to the innate
            difficulty in using the internet to confirm your personal perspectives. The first thing to do, would be to look around the web for reviews and
            people's experiences in the gym. Of course, people will have disparate experiences, so it will be
            neccesary to take them with a grain of salt.
            <hr>
            <b>Q: What would you say is the best way of telling whether a school or gym can be trusted?</b>
            <hr>
            <i>Sensei Seth: That's a tough one! The trial gives you a pretty good idea [of the school's competency].</i>
            <hr>
            The famous instructor also highlights how one should observe the sparring. They shouldn't be <i>"taking each
            other's heads off"</i>. It is fine if the sparring is very light, but it is crucial that the sparring is technical.
            That is to say, students are encouraged to explore and use a variety of techniques that they are taught, and are
            given advice on improving their fighting skills whilst they do it. An example of such advice could be corner advice at
            a boxing gym.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/light_sparring.jpg" height = "auto" width = "100%">
            <i style = "color: gray;">In Thailand, Muay Thai sparring is much lighter than in western countries, as
            most people who train in this brutal fighitng style there, do it to support their families.</i>
        </div>
        <hr>
        <h3>Avoiding Bullshido</h3>
        <p>
            'Bullshido' can take on many forms. Sometimes, the instructors talk about "ancient techniques" that have been
            passed down over "generations", or techniques which are "too dangerous to spar with". A key component
            to consider, which was mentioned in the last section, is the sparring. For some styles, it is common to not have sparring.
            Aikido, for example, tends to prefer <i>Randori</i> over sparring. But walking in to modern combat sport gyms with no
            sparring whatsoever, is a huge red flag. It depends a lot on the style being taught and the environment.
        </p>
        <br>
          <div class="iframe-container">    
            <iframe src="https://www.youtube.com/embed/KgKY71iiJsQ" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
          </div>
          <i style = "color: gray; text-align: justify;">As a side note, competely avoid any styles that talk about chi or ki as some sort of mystical force...</i>
        <br>
        <br>
        <p>
          I have personally made sure to also take a look at the online presence of different schools before taking trial classes.
          The content on their facebook pages, or their website (if they have one). One time, whilst checking out a website for
          an academy I was thinking of trying out in, I came across a paragraph which claimed that the main instructor was a
          black belt in Muay Thai (Muay Thai doesn't have a belt ranking systems). It's always worth checking the 'about me' tabs
          on martial art schools' websites.
            <hr>
            <b>Q: Are there any common tropes in Bullshido schools that you can name or pick out?</b>
            <hr>
            <i>Sensei Seth: [Not showing the qualities that will make a school trustworthy]. Some
              of the things you'll see are signing people on for super long contracts before they've even taken a class.
              Having people buy gear before people can spar, or not allowing people to buy their gear [from other sources aside
              from the school].
            </i>
            <hr>
            <p>
            Some other common tropes that he went on to talk about were the aforementioned "secret techniques" and also,
            not allowing people who weren't part of the school to see what is going on inside and a lack of free trials. <hr>
            <blockquote>
            <i>Pretty much anything that makes them seem insecure about what they are teahing - Sensei Seth</i> 
            </blockquote>
            </p>
        </p>
        <hr>
        <p>
            Finding a good place to train, and a good style to train, can be very difficult. Once you amass expereince, it will be
            much easier to do so - you'll be able to tell whether a school is worth its salt almost immediately. However, as a
            beginner, it is best to rely on intuition, online reviews and guides to help you pick out what is best for you. 
        </p>
