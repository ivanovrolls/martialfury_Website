    <h2>The Key Component: Intent</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
            Intent. The purpose behind which an individual acts on their current task or objective. This particular concept has 
            been discussed throughout the history of sport under multiple titles: purpose, conviction, will etc.
            I have always wondered how important the role of this specific attribute is in fighting and martial arts.
        </p>
        <br>
        <p>
            One of the topics that led to me thinking about intent was my investigation of Mexican style boxing. This
            style of boxing is characterised by its "seek and destroy" mentality, in which its practitioners purely focus
            on offense at the cost of defense - they sacrifice their own safety completely, for
            the chance to obliterate their opponent. I began to wonder whether it had to do with the mindset of the fighters - 
            why did they take such a risk?
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/mexican_style_canelo.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            There are plenty of boxers out there who win boxing matches
            by relying on the points system and outboxing their opponents, instead of aggressively rushing in for knock-out
            blows. KOs are also possible whilst still maintaining solid and technical defense - a great example of such
            a fighter is Thomas "The Hitman" Hearns AKA The Motor City Cobra. Hearns, hailing from the Kronk
            Boxing Gym in Detroit, was known for his cracking right cross which landed him several knockouts. But what people
            didn't know is that he also excelled in defense and technique. A great fight showing this was
            his fight against Sugar Ray Leonard.
        </p>
        <hr>
        <p>
            What's important to note is the desire
        </p>
        <hr>
        <p>
            Regardless of this, both mindsets can lead to success in the ring. After all, there are plenty of succesful fighters
            who step in to outwit their opponent by scoring points with no ill-intent towards them. But the reason I find
            intent so interesting in the topic of attaining strength, is due to its role in training. In many, more traditional,
            martial art styles, board breaking has been used as an exercise for conditioning, speed, and measuring one's
            progress.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/board_break.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            I realised a very interesting fact after breaking countless numbers of boards in training - board breaking is not a
            test of one's physical abilities, but psychological attitude. Not much has changed in terms of physical 
            strength during my years in Taekwondo. I also didn't break enough boards to have hardened my knuckles. It was during
            one of my board breaking sessions that I noticed that what was stopping my attempts at breaking the boards in front
            of me, wasn't my physical abilities, but my fear - and in my case at least, fear can lead me to lack of conviction.
        </p>
        <hr>
        <p>
            The reality is, that anyone can break a board. It is a purely psychological exercise, and almost all of us
            have the capabilities to do so. Breaking a single board is intimidating for beginners; their fear of pain
            or failure causes their conviction or intent to falter. It is much more difficult to place conviction behind
            something if you unequivocally believe pain will follow. The only thing that stops someone from breaking
            a board, is their belief in whether they can do it or not. Henry Ford said it himself.
        </p>
        <hr>
        <blockquote>
            "Whether you think you can, or think you can't, you're right." - Henry Ford
        </blockquote>
        <p>
            Breaking boards is about surpassing this feeling of doubt and insecurity. It's about withstanding the pain, learning
            not to fear it, and learning how to be confident in one's choices and actions, regardless of their consequences.
            This is why I believe that intent is something that can be implemented in our daily lives too. When faced with
            difficult tasks, it is good to remember why we are working to overcome then. It might not make the task itself
            any easier, but it will make you more resilient.
        </p>
        <hr>
        <p>
            Whilst training in Shorinji Kempo at my local club, one of the most impactful things I have heard my instructor say
            was: "If you're going to be wrong, be wrong with conviction". The importance of intent and conviction in a fight
            is unimaginable, as it is for daily life too. A fighter must have the intent and desire to attain victory,
            regardless of their methods, and no fighter can be succesful without it.
        </p>
