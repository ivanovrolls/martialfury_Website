    <h2>Krav Maga - Effective or not?</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
    <p>
        Krav Maga is a combat system designed to take inexperienced people
        and turn them into close, hand to hand combat prepared individuals in short amounts of time. It was specifically designed
        to train soldiers by taking the basics of many martial arts (including Judo and Sambo) and is now
        used by the Israeli Defense Forces.
    </p>
    <hr>
    <p>
        I trained in Krav Maga myself for a while and was surprised by lack of sparring. I found this
        very curious, as I do believe that sparring is one of the most essential components
        for learning hand to hand combat, as it facilitates the pressure testing of your skills and helps
        you see what works and what does not.
    </p>
    <hr>
    <div class = "centre_image">
      <img src = "../images/krav_maga_example.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        Sparring is an option for combat sports, but Krav Maga utilizes dirty tactics such as eye gouging
        and groin kicks as the brunt of its attacks. Nevertheless, Krav Maga introduced training exercises that, 
        in my opinion, many martial arts could benefit from. Krav Maga uses drills and exercises that allows one 
        to practice what they have learnt, without suffering any damage themselves. This is done by following 
        simple rules when practicing the techniques:
    </p>
    <ol>
        <li>The attacker chooses any moment to attack which they see fit</li>
        <li>The attacker slowly builds up speed and power with each repetition</li>
        <li>The defender must use maximum aggression in his technique</li>
    </ol>
    <p>
        A chief advantage I have noticed in Krav Maga when compared to combat sports or martial arts, is the lack of
        strict curriculums. It completely revolves around adapting yourself to the situation. To be clear,
        many martial arts curriculums require you to learn a set techiques for specific scenarios, and then judge your ability
        by how effectively you can apply said technique. In contrast, Krav Maga judges you based on whether you succesfully
        defend or attack, not on how you've done so.
    </p>
    <hr>
    <div class = "centre_image">
      <img src = "../images/aikido_Ex.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        Krav Maga also eliminates a specific problem caused by adherence to such stern curriculums. When practicing a new technique
        in traditional styles, the person you practice with usually shows little to no resistance. This is only useful
        for demonstration purposes. A
        clear example in my past experiences was when practicing techniques in my classes for Shorinji Kempo and 
        traditional Jiu-Jitsu. It felt
        natural to have my partner slow down their movements and provide no resistance to my locks when I was a beginner,
        but this seemed to be a trope with the more experienced students as well.
    </p>
    <hr>
    <p>
        In traditional martial arts that train by repetition, this results
        overconfident students who rely on techniques that they have only ever applied to willing and consenting partners.
        Some might argue these martial arts have randori. But randori is a questionable method.
        Aikido is a soft-style, Japanese martial art, but its efficacy is seen as questionable by many in a
        real-life scenario. 
    <br>
    <div class="iframe-container">
      <iframe src="https://www.youtube.com/embed/LMPTk3qdk6I" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
    </div>
    <p style = "color: grey; text-align: center; font-size: 15px;">Randori is an exercise which involves defending a series of random attacks from
      multiple attackers</p>
    <br>
        
        The reason for Aikido's reputation is not just its philosophy and techniques, but that its
        randori demonstrations prove that the training provided by traditional martia arts is not
        appropriate to prepare modern generations for self-defence. This is shown in the video above as well as many others - 
        although this exercise is meant to simulate real-life scenarios, modern randori involves unrealistic and slow attacks,
        as well as the defender's techniques not being resisted.
    </p>
    <hr>
    <p>
        The approach of constant repetition and randori may have worked in feudal Japan, where the practitioners were
        mostly warriors and Samurai. They dedicated hours daily to such training, as
        evidenced by the many pieces of literature provided from the era, e.g. The Book of Five Rings. 
        Krav Maga drills involve randori too, albeit with resisting opponents and techniques with full speed and aggression.
    </p>
    <hr>
    <p>
        Krav Maga also minimses the amount of time needed to be proficient in its use, by using natural human instinct and reflexes
        as an advantage, instead of seeing it as an inconvenience and drilling against it repeatedly. This is a flaw in every
        martial art and combat sport that I have viewed footage of or have experience in. Karate, Taekwondo, Boxing, Muay Thai -
        all of them have their own different blocking exercises that need to be drilled until they became a reflex.
    </p>
    <hr>
    <div class = "centre_image">
      <img src = "../images/karate_block.jpg" height = "auto" width = "100%">
    </div>
    <hr>    
    <p>
        In contrast, this modern combat system uses the natural instinct we have when we are about to be hit in the face -
        we cover up with our hands. No complex inner forearm blocks like in Karate, or circular deflections like in Aikido.
    </p>
    <hr>
    <p>
        Moreover, the training comes with the best of modern combat sports. Pad exercises, technique drills, and modern fitness 
        preparations that many times are based on military standards.     </p>
    <hr>
    <div class = "centre_image">
      <img src = "../images/me_krav.jpg" height = "auto" width = "80%">
    </div>
    <p style = "color: grey; text-align: center; font-size: 15px;">Me (left) drilling in a Krav Maga session when I was 16-17</p>
    <hr>
    <p>
        The mentality that Krav Maga instills in its practitioners, the one of adaptation, is crucial yet lacking, in many
        traditional martial arts dojos. Thus, its safe to conclude its training methods are better adapted to the people of today. 
        Curriculums based on repetition
        (found in every traditional style) are bound to fail in the modern world as the majority of us simply cannot dedicate
        enough time to learn in this manner. Krav Maga's methods and philosophy offer a better option for many of us, that I believe
        should be adopted not just by traditional dojos, but by modern self-defence curriculums too.

    </p>
