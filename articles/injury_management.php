    <h2>The importance of injury management and prevention</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <p style = "color: gray">(14/10/2021, by Ivan Germanoff)</p>
    <hr>
    <p>
        I am no stranger to injuries. I’ve suffered many throughout my experience with sports. 
        I am writing this article so that you can learn from my mistakes, and hopefully, avoid the stress that 
        is accompanied by the pain of sports injuries.    
    </p>
    <hr>  
    <p>
        Every athlete experiences the guilt of missing a workout. Many say it’s unhealthy, 
        but I believe that not only does it help to keep me disciplined, but that it’s unavoidable.
        However, this guilt seems to be magnified when being unable to workout due to injuries or other personal
        issues. Uninvited thoughts begin to pervade your mind when you find yourself in such circumstances.
    </p>
    <hr>
    <p><i>“If I was as dedicated as I claim to be, I would train through the pain of my injury”</i></p>
    <hr>
    <p><i>“Being injured is not an excuse to stop training”</i></p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/knee_injury.jpg" height = "auto" width = "100%">
    </div>
    <hr>  
    <p>
        These thoughts, as I’ve come to learn from the experience of my acquaintances,
        are natural. But one thing that helps me to keep these thoughts at bay is reminding myself of this
        simple fact – recovery is training too. You should be proud to have such thoughts:
        though they can feel agressive or damaging, they can serve as a reminder of how much work you have put into
        strengthening your discipline and mindset. </p>
    <hr>
    <p>
        In modern media, such as books, series and movies, there
        is a recurring theme of protagonist and antagonists alike, taking their training to extremes, which
        can be a very inspiring image. Nevertheless, it can be harmful one too.
    </p>
    <hr>
    <p>
        Such footage can easily inspire you to do the same, but it’s easy to forget the other important aspects
        that come into question when sculpting perfecting your mind, body and skills. For example,
        when was the last time you saw a sports-based inspirational movie with rest days?
        Furthermore, there also isn’t much media that portrays the difficulty of maintaining a dieting regime. 
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/rocky_pushup.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        As inspiring as training montages in the all-time classics (e.g. Rocky) might be, it is of 
        utmost importance to keep realistic expectations on managing your body. Rest days are necessary to recover and build muscle. 
        Sleep is needed to keep your mind healthy, and accurate nutrition is also needed to ensure 
        your body has the resources to build upon itself. 
        Injuries are commonly caused by overlooking these basic factors.
    </p>
    <div class = "centre_image">
      <img src = "../images/baseTriangle.png" height = "auto" width = "55%">
    </div>
    <hr>
    <p>
      Beginners and experienced people alike will make the mistake of taking their performance and management of the basics
      for granted, whilst putting all their time into minute details. For example, many individuals will 
      spend hours of focus on micro-managing their supplements, such as protein
      powders and creatine to maximise their progress, but then completely block out the fact they go to sleep at 2 am 
      and wake up at 8 six days weekly. Next follows the classic question: why am I not improving?
    </p>
    <hr>
    <p>
      In fact, I can cofidently state that lack of progression or improvement, as well as injuries, stem from a disbalance between these 
      three factors.
      Omitting one or two factors and placing all your stress on the other, or something that is not included in the
      Base Triangle is a recipe for a lack of progress, and in the worst of cases, injuries.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/ath_injury.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        You will lose out on more training if you injure yourself than if you were to take a day off. 
        Balance is key. Consider the factor of rest, just as much as you consider diet and exercise.
        I was diagnosed with patellofemoral pain syndrome; when the tissues under the patella become agitated from overuse.
        This happened to me because I found it too hard to concede rest days for myself. I would wake up,
        stretch, and practice kicks for hundreds of repetitions daily, runs and weight training.
        Eventually, I woke up with a weakness in my knees that made it uncomfortable to walk or stand still,
        let alone kick.
    </p>
    <hr>
    <p>
        Avoiding injuries is simple: listen to your body. Learn what is and isn't normal for your body.
        Waking up the day after a gruelling chest workout at the gym, it is natural for your body to feel
        sore. However, whilst doing an exercise, such as a squat, it might be unusual for you to experience a sharp pain
        in your knees. 
    </p>
    <hr>
    <p>
        It would be very tempting to push through the pain,
        but it is important to stop instead. I would like to emphasise, that it only takes one bad rep for you to injure yourself.
        Substitute your exercise for something that doesn’t cause pain, or better yet, 
        rest for the day. Take anti-inflammatories before going to bed.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/mike_tython.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        I also recommend buying cod liver oil capsules. Cod liver oil, specifically the richness in its Omega-3 fatty acids it
        provides, has been shown to reduce chances of chronic inflammation. This factors in diet too. Avoid foods
        that might make you feel tired or full before a workout, so as to maximise your concentration on correct technique. Also,
        do make sure that you eat and rest enough to handle your training's toll on your body.
    </p>
    <hr>
    <p>
      Managing injuries and living with them, can be a bit more difficult. The principal reason for this is the psychological,
      not physical, damage that injuries can bring. In cases where injuries keep an athlete from any sort of physical training,
      it can result in a disassociation with their identity. For many athletes, their sport serves
      as a key characteristic in shaping their personalities and who they are. It's common to fall into depressive stage in
      which feelings of demoralisation set in.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/guilt.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        What one must understand is that there is always a
        method of self-improvement, regardless of circumstance.
        Training in the Base Triangle includes not just physical exercise, but mental preparation too.
        Michael Jordan watched more tape than anyone. Just because you can't train your body doesn't mean 
        you can't be involved in your sport.
    </p>
    <hr>
    <p>
        It’s understandable to be discouraged when injured but attempting to train through it will make it worse. There are many recorded cases of
        jiujiteiros training through broken fingers – their
        dedication is admirable, but an injury that might be healed in just a couple of weeks, becomes something they live with
        and throws them off their game. Give yourself time.
    </p>
    <hr>
    <p>
      While you are healing, take your time to watch tape or instructional videos on any areas that interest or that you wish to 
      improve upon as soon as you're good to go. There is plenty of BJJ submission and guard passing tutorials online,
      as well as PDFs of historical books that can help strengthen your mindset and philosophy, such as The 48 Laws of Power
      or The Art of War. If you can't train your legs, train your arms. If you can't train your body, train the mind.
    </p>
