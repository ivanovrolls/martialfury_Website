    <br>
    <hr>   
    <p>
        Unfortunately, that is how these styles are. The original practitioners were extremely strict, and there are accounts 
        of beginners being forced to solely practice the Sanchin kata for several years before even being allowed to move on to
        technique development, in some styles of Karate. Older arts relied on sheer repetition and the extreme fine evolution
        of muscle memory. On the flip side, modern combat sports train through a large variety of drills, 
        exercises, and sparring.
    </p>
    <hr>
    <p>
        The old methods are just not compatible with the modern age that we live in - we are simply too 
        impatient to learn this way. Our information is spoon fed to us in two-sentence tweets,
        and 15 second TikTok clips. Our attention span and patience have been artificially worn thin to such an extent that 
        is nearly irreversible; it could be argued that it would be much more difficult to train using the original methods
        in this day and age, than it was back in the day.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/tkd_poomsae.jpg" height = "auto" width = "100%">
    </div>
    <hr>    
    <p>
        However, this is not meant to imply that the old way is superior. Although personally, I tend to lean towards
        older training methods (especially technique advancement exclusively through repetition), there are many modern 
        movements that I feel would be much more difficult to learn without the newer exercises. For example, can you imagine drilling
        BJJ without a training a partner, and only through repetition drills on a training dummy? It simply wouldn't be the same.
        Although older styles also had partner training, they did not place nearly as much emphasis on this - this is evidenced simply by the existence
        of forms.
    </p>
    <hr>
    <p>
        Forms were specifically made to pass on teachings from one generation to the next - a master would teach his student a form, and 
        this student would eventually mature to become a teacher themselves and teach the kata to their students. But you don't see this
        in modern arts. There are no specifically designed patterns of shadow boxing, or hard-coded forms in catch wrestling. In my own words,
        older arts relied on what I like to refer to as "unidimensional training" whereas modern arts rely on "multidimensional training". 
    </p>
    <hr>
    <p>        
        This distinction exists
        as a result of many factors; nowadays it is easy to look up technique and information on the web, whereas in these bygone eras,
        such information was spread by word of mouth, books, or kata. Kata, apart from training exercises, were a way of passing on knowledge
        from teacher to student - each kata has its own lesson to impart upon its practitioner, such as the Taikyoku kata's lesson on basics.
        Gichin Funakoshi stated this kata to be the most important training tool; its name means "Basic Ultimate", implying its basic yet important nature.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/shadow_boxing.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
        Unidimensional training is the type that requires no equipment or training partners at all - this was the reality for many practitioners;
        we have the luxury to train, should we wish to, in a martial art during our free time, whether as a hobby or
        for competition. In contrast, during the times of Okinawan Karate, instructors were commonly very poor and needed to have another job apart from 
        teaching to survive - not many people could afford to train, so many of these teachers would train alone for extended periods of time.
        They would even resort to picking street fights with Yakuza and other trouble makers in order to test whether the techniques they had developed 
        were effective in combat.
    </p>
    <hr>
    <p>
        Multidimensional training is what we have in all modern combat sports: Sambo, Boxing, Sport Taekwondo, BJJ and Wrestling and more.
        All of these styles rely very heavily on training with a partner. Bag work, sparring, drilling and more. In grappling,
        it is fruitless to even demonstrate a technique without another individual to help. Striking arts such as Taekwondo, some aspects
        Sambo, and Boxing, do allow for training on your own, albeit unidimensional training.
    </p>
    <hr>
    <p>
        A healthy, balanced mix of both is always recommended. Unfortunately, that is not alwayss easy; as stated above, some arts 
        are simply better for unidimensional training than others.
    </p>
