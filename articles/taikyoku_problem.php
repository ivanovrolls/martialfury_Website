    <h2>The Taikyoku Problem</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
    <p>
        As time passes, and generations go by, it is undeniable that the world of martial arts has undergone a lot of
        changes since the days of traditional martial arts, such as Karate and older styles of Kung Fu. The world
        of fighting and techniques has changed - we have moved from knuckle conditioning to bulgarian bag exercises,
        and from makiwara straight punches to heavy bag rounds.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/sandra_sanchez.jpg" height = "auto" width = "100%">
        <i style = "color: gray;">Spain's Queen of Kata, Sandra Sanchez</i>
    </div>
    <hr>    
    <p>
        I recently began to question why it seems that traditional martial arts are less popular, and are becoming so
        day by day. Less than a century ago, the efficacy of Karate for self-defence went by almost unquestioned,
        and martial arts practioners would devote themselves completely to the style they trained, instead of mixing and
        matching techniques from different ones. The reason Bruce Lee is seen as the father of modern MMA is due 
        to his philosophy of combining different martial arts instead of devoting yourself to one "way".
    </p>
    <hr>
    <p>
        As I read up on the dwindling popularity of traditional styles of self-defence, I encountered a forum post on this issue,
        titled "The Taikyoku Problem", in which the poster claims that these all of these older styles have a similar issue, in that
        the way they impart teaching to begginers is through very dull and boring kata or forms. He specifically points out
        the Taikyoku kata in karate - the kata, in the writer's own words, is boring, with simple patterns and unrealistic movement.
    </p>
    <hr>
    <p>
        This kata is designed for beginners so that they may get accustomed to the movements, but the writer claims that 
        it alienises them instead, as they begin to find the kata dull very quickly, sapping them of their motivation and willingness to
        continue their training.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/trad_karate.JPG" height = "auto" width = "100%">
    </div>
