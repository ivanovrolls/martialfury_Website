    <h2>The importance of accountability for attaining mental fortitude</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
    <p>
        Mental toughness is one of the most important tools professional athletes can have in their arsenal, and it can be just as
        useful to anyone regardless of whether they dedicate themselves to sport, studying, or their job. Mentally tough people
        tend to be more critical of themselves – <b>this might sound akin to insecurity, but it is a good type of insecurity</b>.
        It allows us to be completely honest when we ask ourselves whether we can improve, and how we can do better. This honesty
        is what separates winners from losers. It concedes the power to push through obstacles, whether they be physical,
        psychological… or even worldwide obstacles such as the pandemic.
    </p>    
    <hr>
    <div class = "centre_image">
        <img src = "../images/brain_stronk.jpg" height = "auto" width = "60%">
    </div>
    <hr>
    <p>
        It is said that only mentally weak people allow their excuses to get in the way of achieving their goals -
        that excuses only hold you back because they bar you from reality. They are akin to participation trophies –
        they shield you from experiencing failure and comfort you with thoughts that are characteristic of weak mindsets,
        such as <i>'trying is enough'</i>. Tim S. Grover, the man who has trained legends such as Kobe Bryant and
        Michael Jordan, stands by this personally.
    </p>
    <blockquote>
        "Unsuccessful people make excuses, blame everyone else and never get past the deficiencies" – Tim S. Grover
    </blockquote>
    <p>
        What leads men and women to developing prowess is accountability. Many people fail to take responsibility for their actions
        and instead, as the quote above says, make excuses as to why they failed. A student might fail an exam, and instead of
        admitting they didn’t study hard enough, they might chalk their grades up to chance. They might claim it’s because of
        their siblings distracting them while studying, or that the questions asked of them wasn’t in their learning material. 
    </p>
    <hr>
    <p>
        Accountability is a very empowering tool because it helps you realise that you have more control over situations than you
        imagine. David Goggins, known as the “Toughest Man in The World”, explains accountability and his experience with it in
        this interview:
    </p>
    <br>
    <div class="iframe-container">
        <iframe src="https://www.youtube.com/embed/aXo1EgoQu9E" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
    </div>
    <br>
    <p>
        <b>Accountability starts with internal honesty</b>. It is important, as the Toughest Man Alive claims, to be completely
        honest with yourself. Once you do this, you will figure out ways to make yourself better. In contrast, lying to one’s self
        leads to weak mindsets, and weak mindsets in turn lead to an unsatisfying life. It is near impossible to lead a fulfilling
        life if one does not aim to improve themselves. How can your quality of life improve, if the quality of your character 
        doesn't?
    </p>
    <hr>
    <p>
        Unfortunately, we live in a world that values self-acceptance over self-improvement. If people are obese, they are told
        that they are beautiful. If people lose competitions, they are awarded participation trophies. This is garbage perpetuated
        by people who refuse to improve upon their imperfections. It’s why we have clinically obese women in magazine covers and
        claim they’re models. It's also why we disguise truthful words with sugarcoated, and artificial variations:
        Being fat is now called being “plus sized”. It is much easier to live with your flaws than to correct them.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/plussizedmodel.jpg" height = "auto" width = "50%">
    </div>
    <hr>
    <p>
        This isn’t to say that you should strive to be perfect; everyone has flaws, and some might be incorrigible in the short
        or long-term. But it is never a bad idea to strive to be better. A commonly cited way to do this by many motivational
        speakers is to try to do one thing to improve yourself every day. It could be morning stretches to make sure you stay nimble
        for the day, a workout, meditation or reading a book. 
    </p>
    <hr>
    <p>
        It is also good to strive to do things when you don’t want to do them. Try to control your desire to procrastinate and sort out your
        priorities. There are some tasks you simply cannot put aside. There is a reason why mentally tough people stay on top
        of things, and that's because they are almost constantly thinking about how to improve themselves or the environment around them.
    </p>
    <hr>
    <p>
        The conclusion to take from this article, is to rise above the status quo of lying to yourself. Be honest.
        Take accountability for who you are and where you are now. Look into the mirror: does your body look like this because of
        your dietary and fitness choices, or someone else’s? How can you improve yourself as a person? Is your hygiene lacking?
        Could you shave an extra pound or two?
    </p>
    <blockquote>
        "When there is no enemy within, the enemy outside can do no harm" - African Proverb
    </blockquote>
    <p>
        Psychological prowess leads to physical prowess. It leads to a better quality of life, an improvement in performance
        and in your mental health. Don’t allow other people to set your terms, set them yourself. An
        overwhelming amount of people have the capacity to achieve their definitions of success – an underwhelming minority ever do.
    </p>
