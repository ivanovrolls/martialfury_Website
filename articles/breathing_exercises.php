    <h4>Straw Breathing exercise</h4>
    <p>
        Old Russian military dive schools had an exercise to teach soldiers economic breathing for conserving air in their dive tanks.
        This exercise improves lung capacity and cardiovascular endurance by teaching you to control your breathing and extend
        your breaths. This is most useful when air feels limited e.g. heavy cardio. To do this exercise, you must lay on the ground with a straw in your mouth,
        breathing only in and out of the straw. 
    </p>
    <hr>
    <p>
        Once you become accustomed to this, tape another straw to the end of the first straw and then breathe through the extended
        straw in the same manner - repeat the process again until you are ready to extend the straw a second time (3 straws total).
        Your goal is to be become accustomed breathing solely through this long straw; you will be forced to learn how to extend 
        and calm your breathing. Calming your breathing is the best way to recover from heavy cardiovascular training sets.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/straw_breathing.jpg" height = "auto" width = "100%">
        <i style = "color: grey;">Never breathe through more than three straws taped together, as you will only breathe the air you have already expelled.</i>
    </div>
    <hr>
    <p>
        This will be difficult, as the amount of air you can take in at a time, and the amount you can expel, will be
        drastically limited.
    </p>
    <hr>
    <h4>Ratio breaths</h4>   
    <p>
        Anyone who has ever done cardio heavy sport can describe the experience of attempting to recover from a set. Your 
        breathing is panicked, your throat is dry and you can feel your heartbeat thumping 
        in your ears. Your body is panicking and doing its best to recover energy by hyperventilation. However, hyperventilation
        is damaging to your recovery time. The last exercise showed you how to control your breathing, but this is how you 
        can train yourself to control your breathing when recuperating from intense training.
    </p>
    <hr>
    <p>
        Ratio breathing is simple; take the amount of reps you have just performed in your last set, and apply a ratio for
        the amount of breaths you will take before beginning your next one. For example, if I have just performed 
        10 reps of heavy kettlebell swings (a highly recommended exercise), I will apply a 2:1 ratio, 
        and take, at most, 5 breaths before beginning my next set.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/tired_athletes.jpg" height = "auto" width = "100%">
    </div>
    <hr>    
    <p>
        This exercise is a modern form of <i>Shinkokyuu</i>, a breathing method performed by traditional (and some Kyokushin style)
        karatekas. Looking around a room of tired karatekas, you may spot some of them having a deadpan look on their face. They 
        are focusing on performing deep, abdominal inhalations to help them extend their breaths. Skip to the last exercise if you
        wish to learn about abdominal breathing.
    </p>
    <hr>
    <h4>Breathing 'behind the shield'</h4>
    <p>
        Breathing behind the shield is the concept of breathing whilst keeping your core muscles tensed. This is a type of breathing
        that is most beneficial to those in grappling arts and powerlifting or weightlifting competitiors. Breathing is necessary for all 
        activities, and the ability to breathe whilst maximally tensing your core is very advantageous for increasing your endurance
        when supporting or lifting heavy loads (e.g. framing attacking opponents in BJJ, or squatting heavy ass weights).    
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/bjj_framing.jpg" height = "auto" width = "100%">
    </div>
    <hr>    
    <p>        
        The best way to learn this is from experience - focus on keeping your core braced as tight as possible whilst performing 
        such activities. I personally recommend slowing down a heavy lift between 3-5 seconds, and focus on squeezing your glutes 
        and abdominal muscles as tight as possible.
    </p>
    <hr>
    <h4>Abdominal breathing</h4>
    <p>
        This is easily the most important exercise on the list - I have saved the best for last. Applying abdominal breathing 
        will benefit you in all types of ways, from endurance to striking speed. It is a skill that 
        is heavily emphasised in Karate - one of the biggest proponents for abdominal breathing is Karate legend, and founder
        of the Kyokushin style, 'Godhand' Masutatsu Oyama. He earned his nickname after fighting 52 bulls barehanded and chopping
        off their horns with his bare hands.
    </p>
    <hr>
        <div class = "centre_image">
            <img src = "../images/mas_oyama.jpg" height = "auto" width = "100%">
            <i style = "color:grey;">The Godhand, Masutatsu Oyama</i>
        </div>
    <hr>
    <p>
        Oyama spent much of his time on breathing techniques, namely Ibuki and Nogare. These techniques are complementary;
        one is fast, the other is slow. One is hard-style, and the other is soft-style. Regardless, both utilise abdominal
        (or diaphragmatic) breathing. When learning to breathe with the abdomen, it is important not to use your throat
        muscles to force the air out; breathing must not be forced. Instead, contract your glute and core muscles 
        to squeeze the air outwards.
    </p>
    <hr>
    <p>
        Abdominal breathing can make your strikes and lifts faster - a relaxed limb is a fast limb, and breathing with your chest
        creates tension in your arms and upper body. Learning to use abdominal breathing in combination with the other 
        breathing exercises above, will improve your grappling endurance during actions such as framing, your lifting speed 
        and stability, and your ability to withstand body shots in striking arts, even more.
    </p>
    <hr>
