    <h2>Breathing exercises to improve performance</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
    <p>
        Breathing is essential - to martial arts, sports, and to life in general. The way that you breathe 
        directly impacts your performance in cardiovascular exercise, and your recovery rate from it. Surely then, it makes sense
        to improve your breathing technique?
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/breathing_lungs.jpg" height = "auto" width = "75%">
    </div>
    <hr>    
    <p>
        In this article, I will be outlining a number of breathing exercises, along with their benefits, history and 
        manner of application.
    </p>
