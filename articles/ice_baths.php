    <h2>No such thing as extreme #1: Ice baths</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
            Ice baths. The cold shower’s big, bad and burly brother. Used by fighters as well as athletes across the world as
            a method of therapy, ice baths are perceived to be “hardcore” or “extreme”. Such imagery is engrained in our heads
            due to its references in many different action movies such as Creed II, or the plethora of videos online of famous
            celebrities and athletes filming their experiences with it. But where did ice baths actually originate from,
            and how exactly do they work?
        </p>
        <br>
        <div class="iframe-container">
            <iframe src="https://www.youtube.com/embed/gXB99JeGSBo" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <br>
        <p>
            This method of hydrotherapy (therapy by water) dates as far back as Egypt around 2500BC, where it was used to treat
            cuts and injuries. However, Hippocrates (a very famous Greek physician, well known in the field of medicine) is
            often credited as being the first to specifically document the benefits of this method. A common misconception is that
            ice baths are a relatively new method of treatment even though <b>its use is also noted amongst ancient Chinese and Roman 
            civilizations.</b>
        </p>
        <hr>
        <p>
            There have been a number of benefits that have been investigated as a result of ice baths. Due to the cold temperature 
            in which one submerges oneself, the surface and core temperature of the body decreases – this leads to a <b>decrease
            in swelling and promotes the healing of acute inflammation muscle damage</b>. It also helps reduce stress through a
            decrease in cortisol, the hormone in your body that causes stress.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/ice_baths.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Personally, however, I believe that ice baths are better for the mind than they are for the body. Though they may have
            some benefits for healing, ice baths and cold showers <b>should be mainly used for building psychological and mental
            strength</b>. Not many people can honestly say they enjoy submerging themselves in ice cold water; this is precisely
            why it’s beneficial for the mind. By actively making the choice to do so, you are reinforcing a mentality of
            perseverance, discipline, and strength. You are forcing your body out of its comfort zone – and <b>discomfort leads
            to strength</b>. No one ever became strong, or fast, or adaptable by doing what was comfortable.
        </p>
        <hr>
        <p>
            Ice baths can also be used to lose weight as the body must burn calories in an attempt to bring your body temperature
            up; this is partially the reason why swimmers burn more calories than runners, as their bodies also use up calories
            in maintaining their body temperature whilst swimming in cool water.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/butterfly_swim.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Regardless of all the benefits, <b>ice baths can be dangerous</b> – especially for people with pre-existing conditions.
            The decrease in core temperature and constriction of blood vessels slows blood flow, and if your blood flow is already
            slower than usual due to other reasons, it could place you at risk of cardiac arrest. Moreover, any exposure to very
            low temperatures carries the risk of hypothermia – this is why most experts agree that an ice bath should take 15
            minutes tops, and why professional athletes have special hire to regulate their body temperature and other factors.
        </p>
        <hr>
        <p>
            Lastly, in 2015, the Journal of Physiology published an article claiming that cold water immersion reduces the efficacy
            of strength training. In one study, 21 (physically active) men trained for strength over the course of 12 weeks, for
            2 days a week, with either 10 minutes of cold-water immersion after their workout, or active recovery instead.
            Strength and muscle mass supposedly increased more within the group that used active recovery training after
            their workouts. <b>I would urge you to take this study with a grain of salt</b>; a sample size of 21 men is quite small,
            and training was drastically insufficient. According to another study, most Americans go to the gym three times a
            week – this means that the study had the subjects work out less than national average in the USA. For most athletes,
            fighters, and gym enthusiasts, two workouts a week isn’t even the bare minimum. Their diet also wasn’t recorded,
            and it is not made clear what it is meant by “physically active” – the term is simply too relative.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/physically_active_man.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            In conclusion, ice baths have been noted to be very beneficial for athletes. <b>They were even recorded to improve
            performance in sprinters and endurance runners</b>. However, it is a practice that can be quite hard to do for the
            average person – it requires meticulous measurement of the temperature, and an obscene amount of ice. Cold showers
            provide many of the same benefits as ice baths, with less risk towards vulnerable people, and in a way that is
            considerably more accessible. 
        </p>
