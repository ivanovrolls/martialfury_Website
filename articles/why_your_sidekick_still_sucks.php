      <h2>Why your side-kick still sucks</h2>
      <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
      <hr>
        <p>
            I’ve been where you are myself, I can empathize. You feel like you’ve tried everything, and you’ve just finished
            recording yourself doing a side-kick, but when you look back at the recording, you still think it looks… off.
            You can’t understand why. But eventually, I got through it, and I am sure that with this guide, you will too.
            Let’s look at some common issues that result in wonky technique.
        </p>
        <hr>
        <h3>Focusing on kicking height rather than technique</h3>
        <p>
          Many of us love to scroll through martial arts social media pages for “inspiration”, an all-too-common excuse for wasting hours on our phones at a time. But this may do
          more harm than good. How many posts have you seen by these pages which feature someone performing a kick with insane height?
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/sidekick.jpg" height = "auto" width = "65%">
        </div>
        <hr>
        <p>
          Regardless of how great it looks, these kicks are solely for aesthetic purposes. The only time where such height matters,
          is when being judged on poomsae or kata, where points are awarded for kicking height. In terms of
          practical application, any kick above head height (and some would argue above torso height) is pointless.
        </p>
        <hr>
        <p>
          The answer to this issue is to focus on the markers that lead to good technique, and to stop chasing height. Chasing height
          without appropriate flexibility, leads to the sacrifice of good technique by leaning back more than necessary.
          Good side-kick technique involves:
        </p>
        <ol>
          <li>Focus on hip rotation</li>
          <li>Aligning your glutes, shoulder and kicking heel in a straight line when chambering</li>
          <li>Pivoting the root foot, so that your heel is facing the target</li>
          <li>Balance</li>
        </ol>
        <h3>Lack of rotation</h3>
        <p>
          At one point I was under the illusion that if I became flexible enough my technique would drastically improve.
          However, a very simple technical mishap caused me a lot of trouble and it took me a surprisingly large amount of
          time to become aware of it. It is a common mistake that beginners to martial arts make: Not rotating the foot you're
          standing on.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/my_sidekick_2.jpg" height = "auto" width = "75%">
        </div>
        <hr>
        <p>
          In every single kick, the rotation of the foot which you’re standing on (the root foot as I like to call it) must rotate.
          You can see the rotation of my foot in the picture above.
          Some people rotate it, but don’t rotate it enough. Others don't even rotate it all.
          For the side-kick, the heel of the root foot must be facing your opponent. The reason for the rotation is
          because it opens up your hips in a more natural position for you to raise and extend your leg.
        </p>
        <hr>
        <h3>Focusing on stretching, not kicking</h3>
        <p>
          When a boxer has trouble with the execution of his jab, what’s the solution? It’s practice. 
          Start kicking more. Kicks are best improved by
          kicking, punches by punching and flexibility by stretching. These abilities may overlap, but they are not directly
          responsible for each other. Do take it easy though; don't start kicking until your leg falls off.
        </p>
        <hr>
        <p>
          Building technique is about imprinting the muscle memory required for you to execute the movement you are focusing
          on without having to think about it. Boxers with perfect jabs aren't focusing on the markers that build a good jab 
          mid-spar. Their focus is on their opponent. If you want to know whether you have fixed your technique, see if it is
          present under pressure.
        </p>
        <hr>
        <h3>Not enough hip strength/flexibility</h3>
        <p>
          Hip flexibility is needed to be able to raise your leg higher, and hip strength is what allows you to keep it there.
          I use two main exercises to improve these factors:
        </p>
        <hr>
        <p>
          Frog Splits (for hip flexibility):
        </p>
        <hr>
        <img src="../images/frog_splits.jpg" height="auto" width="100%">
        <hr>
        <p>
          Lateral Raises (for hip strength):
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/lateral_raises.jpg" height = "auto" width = "65%">
        </div>
        <hr>
        <p>
          Hip strength is key. It is so often overlooked but it's perhaps the biggest factor when improving everything about your
          kicks - power, height, strength, speed etc. In order to test whether you kick at a height that is good for you, there is a very simple test.
          Take whichever kick you wish to test (different kicks can use different muscles) and simply do it at the whichever height you choose,
          but ensure that you do it slowly. Once your kick is fully extended, try to hold it for 10-15 seconds. If I can't do this, there is only two
          possible reasons:
        </p>
        <ol>
          <li>You are not strong enough to be kicking at the height you are kicking at</li>
          <li>You do not have enough balance to use this technique effectively</li>
        </ol>
        <hr>
        <h3>Not 'blading' your foot</h3>
        <p>
          When you execute a side-kick, you should be making sure your kicking foot is angled with the pinky toe higher than
          your big toe. This provides an emphasis on kicking with the blade of your foot. Some schools or martial styles will
          tell you to kick with the heel of your foot, which is a perfectly valid technique too. However, if you're aiming to get
          your side-kick to look better or flashier, the bladed variant looks better.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/bladed_sidekick.jpg" height = "auto" width = "65%">
        </div>
        <hr>
        <p>
          Notice the kicking foot in the image above. Some styles, such as distinct variations of karate, will teach you both kicks. They claim kicking with the heel delivers
          more punishment, but the blade allows for faster kicks that can be used to counter or interrupt an opponent's attacks. The bladed variant tends to be preferred for more accurate
          strikes too - traditional styles of Okinawan Karate involved bladed kicks to the armpits, targetting lymph nodes.
        </p>
        <hr>
        <h3>Conclusion</h3>
        <p>
          The side-kick can be an arduous technique to learn. This is due to it being one
          of the only linear kicks, in contrast with most other kicks that involve an arching motion. Be sure to check if you're doing everything above, and I gurantee your side-kick will already
          feel much better. It might feel a bit uncomfortable at first, but your body will need time to get used to new movements.
        </p>
