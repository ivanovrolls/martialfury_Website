    <h2>Primal flow</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
            My abs have always been the weakest part of my body - this was highlighted to me during the fleeting time I spent practicing Capoeira.
            Whilst browsing videos on this elusive martial art and the exercises it uses to strengthen the core,
            I came across this one below, and I was completely fascinated by the different movements demonstrated. 
        </p>
        <br>
          <div class="iframe-container">    
            <iframe src="https://www.youtube.com/embed/VDLCaLkzuwo" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <br>
        <p>
            It was difficult to find information on the subject - most websites in my search results just promoted 
            expensive $800 courses! Eventually, I came across some details.
            The aims of this branch of yoga, is to use exercises which emphasize "primal" movements,
            to increase core strength, flexibility and overall fitness. These movements are:
            <ol>
                <li>Pushing</li>
                <li>Pulling</li>
                <li>Twisting</li>
                <li>Crawling</li>
                <li>Bending</li>
                <li>Squatting</li>
                <li>Gait</li>
            </ol>
        </p>
        <hr>
        <p>
            These are known as the Seven Primal Movements. For those unaware, "gait" refers to exercises such as running
            or walking. The exercises used to strengthen these movements are, according to
            <a href = "https://www.kaizencentre.co.uk/activities/primal-movement/">Kaizen Performance Centre</a>, "low impact" and
            "natural". This is perfect for fitness enthusiasts who are interested in forming a connection with the more animalistic
            nature of the human body. 
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/animal_flow_1.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Nevertheless, it can be intimidating to try something new. This is why I recommend this <a href = "https://breakingmuscle.com/workouts/primal-move-workout-1-the-warm-up/"> series of articles</a> for a
            good guide to animal flow workouts.
        </p>
        <hr>
        <p>
            The benefits of yoga are numerous, but yoga isn't for everyone - I am not one to be interested in holding odd looking positions
            for extended periods of time. Yet this type of yoga, feels much more welcoming for martial artists and gym goers alike, as many of the
            exercise seem to be focused on the repetition of movements, rather than the static holding of different positions. Much of the
            more advanced movements are feats of strength on their own. Many of these motions improve flexibility through dynamic
            stretching.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/animal_flow_2.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            To many, animal flow might seem like a rebrand of calisthenics, but with a cooler name. It's a fair way to look at it.
            Both are methods designed to improve functional strength without the neccesity for excessive amounts of training 
            equipment. The chief difference, would be that the way the body moves is completely dissimilar. There are many exercises
            I have found when investigating primal flow, that I have simply never come across during my time researching calisthenics.
            An example of this is scorpion kick ins.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/kickins.jpg" height = "auto" width = "100%">
            <p style = "color: grey; text-align: center; font-size: 15px;">Coincidentally, this is a drill used to warm up in one of the BJJ gyms I train at.</p>
        </div>
        <hr>        
        <p>
            In conclusion, animal flow is a great way to increase strength, mobility, and flexibility of your muscles, body and joints.
            It is also very beneficial for grappling-focused martial artist, as the exercises include the drilling of common positions
            found in these sports, and the strenghtening of the wrestling-related parts of the body.
        </p>
