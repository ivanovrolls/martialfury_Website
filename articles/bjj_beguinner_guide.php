    <h2>The definitive beguinner's guide to BJJ</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
          BJJ is a highly technical martial art that includes an abundance of grappling techniques. There's no way about it.
          The possibilities are endless and your learning never stops. So even just starting BJJ can feel like an unsurmountable
          mountain. This guide is tailored to make your beginning experience as smooth as possible.
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/bjj_cover_img.jpg" height = "auto" width = "100%">
        </div>
        <br>
        <p>
          There are many things that I wish I knew when starting BJJ, so I decided to put all of this information in 
          this article, which is why it will be lengthier than most on this website. This guide will include
          unwritten rules, a summary of some of the more important positions, and beginner techniques to apply from each one.
        </p>
        <hr>
        <h3>The Unwritten Rules</h3>
        <p>
          Like every martial art, especially those that include sparring, there are some rules
          that may never be explicitly stated but should, at all times, be respected.
        </p>
        <hr>
        <h4>Hygiene</h4>
        <p>
          Hygiene is very important for all martial arts, but it is doubly important for arts with close contact such as
          Judo and BJJ. One of the most unpleasant experiences in BJJ is rolling with a partner who doesn’t take care of
          himself. Here are the key points to thing about:
        </p>
        <hr>
          <ol>
            <li>Wash your gi – No one likes to roll with someone with a sweaty and dirty uniform. Try to wash it after each session, or at least, after every 2 or 3 sessions. Many find it helpful to have multiple gis and rotate them every few or so classes.</li>
            <li>Trim your fingernails and toenails – Being cut by your partner’s ungroomed nails is not a riveting experience. Take care of your nails and do it regularly.</li>
            <li>Shower and use deodorant – For many, this is a given. For others, not so much. Shower after every class or bout of physical activity and use deodorant once you have dried yourself. Even better, shower before classes too. It goes a long way.</li>
            <li>Take a mint – This aspect of hygiene is much less talked about. Sometimes, catching a gust of warm and stinky breath is worse than any choke or strangle hold. Brush your teeth regularly and take a mint or gum before class.</li>
          </ol>
        <hr>
        <p>
          These four steps will help to make sure you always have a sparring partner available. Many people find it tough to find partners to roll with because they don’t take care of their hygiene, and they’re always left to wonder why.
        </p>
        <hr>
        <h4>No leg locks</h4>
        <p>
          In many BJJ gym, leg locks are completely forbidden for those under blue or purple belt. This is because they are severely dangerous and have the potential to incapacitate people much longer than other holds. Leg injuries in general are completely different to others – legs are integral part of life that we use almost everyday to walk, run, jog and more.
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/bjj_leg_lock.jpg" height = "auto" width = "100%">
        </div>
        <br>        
        <p>
          Unless you know what you’re doing and have agreed to use them with your partner beforehand (as well as have your instructor’s permission) stay well away from them. Keep an even wider girth from these techniques if you’re a beginner – you don’t want to be the person who took away someone’s ability to walk due to misjudging your ability.
        </p>
        <hr>
        <h4>Manage your intensity</h4>
        <p>
          What does this mean? In a striker’s terms, hit as hard as you want to get hit. Avoid being the guy who asks to roll light, and then crank out explosive arm bars and wrist locks. You’ll garner a negative reputation and people won’t be keen to roll with you. Also, be prepared to reap what you sow. If you roll too intensely with beginners in an attempt to boost your ego, or even just by accident, don’t be surprised when higher level belts begin to pick on you as well. 
        </p>
        <hr>
        <p>
          If you have a habit of rolling intensely, or to tunnel vision, learn to keep yourself in check. Ask your partner to let you know if you’re going too hard or ask your instructor to keep an eye on you.
        </p>
        <hr>
        <h4>
          Conclusion
        </h4>
        <p>
          These unwritten rules will help you on your BJJ journey. No one wants to be the guy who’s constantly left out when the class is asked to partner up. Abiding by these rules will aid you in avoiding this scenario.
        </p>
        <br>
        <h3>Positions and Techniques</h3>
        <p>There are numerous positions in BJJ, which one of the two people rolling will always find themselves in. Nonetheless, there is a certain stage which beginners reach that I reached myself not too long ago, where they find themselves in multiple different positions, but don’t know how to engage. </p>
        <hr>
        <p>
          They might have finally learned to pass guard and take the side control, but how does one proceed from there? Thus, I have attached some tutorials for beginner level techniques to try out from each of the more common BJJ positions below. Here you go:
        </p>
        <hr>
        <h4>Full Mount</h4>
        <p>
          This is a dominant position which involves the aggressor sitting on the hips of his victim. It provides the combatant on the top several advantages such as multiple alleys of attack and the ability to control how the roll or fight proceeds. 
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/full_mount.jpg" height = "auto" width = "100%">
        </div>
        <br>
        <p>
          The mount is a great position to be in. Whether you’re in the street, in a cage match, or just rolling with a partner, there is no denying it is a favorable position regardless of the ruleset. There are multiple ways to submit an opponent from here. The first attacking technique I was taught from mount was the armbar: 
        </p>
        <br>
        <div class="iframe-container">    
          <iframe src="https://www.youtube.com/embed/ip0Hl52JG5A" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <hr>
        <h4>Side Control</h4>
        <p>
          This position is one where the two combatants are chest to chest, perpendicularly, with one on the top and one on the bottom.
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/side_control.jpg" height = "auto" width = "100%">
        </div>
        <br>
        <p>Side control usually doesn’t exert much control over the combatant on the bottom, at least not until the other combatant doesn’t maneuver to pin their limbs, either by kneeling on the closest arm with their leg or squeezing it in between their arm and knee. There are many chokes available from this position, naturally, due to how close one’s arm is around their opponent’s neck:</p>
        <br>
        <div class="iframe-container">    
          <iframe src="https://www.youtube.com/embed/mEqyyTQlZzo" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <hr>
        <h4>Open/Closed Guard</h4>
        <p>
          This position is similar to the full mount, in that it involves one combatant on top of another, except this time, the legs and hips are on the side of the top combatant instead of underneath; sometimes, the feet of the person on the bottom might be locked behind the one on top.
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/closed_guard.jpg" height = "auto" width = "100%">
        </div>
        <br>
        <p>
          There are multiple choices for the fighter on the bottom of this position, but a popular one is the triangle choke:
        </p>
        <br>
        <div class="iframe-container">    
          <iframe src="https://www.youtube.com/embed/CEPp45IenVw" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <hr>
        <h4>Half Guard</h4>
        <p>
          The Half Guard is a risky but common position. It involves the practitioner on the bottom locking on one of the top practitioner’s legs and this sometimes leads to them sliding in a knee shield, whilst the main objective of the top practitioner is to escape the leg lock. The control is 50/50 for both combatants.
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/halfguard.jpg" height = "auto" width = "100%">
        </div>
        <br>
        <p>
          A technique from the bottom when in half guard, is to slide a knee shield in, and attempt to lock in a triangle choke, shown above.
        </p>
        <hr>
        <h3>Finally</h3>
        <p>BJJ offers many more positions, and many guides will usually list another three as part of the basic positions. Other guides will see some of these positions as transitional movements from one position to another. The reason I have specifically chosen these four positions are because I believe they are the best ones to learn first as a beginner delving into this sport for the first time. I would like to think that this guide has been helpful, and that you will be able to apply this knowledge in your adventure with BJJ.</p>

