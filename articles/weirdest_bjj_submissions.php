    <h2>The Weirdest BJJ Submissions</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
    <p>
        BJJ has a vast variety of techniques, given that incorporates them from all grappling arts such as catch wrestling, 
        Judo, Sambo and more. But every now and then, if you delve into the web deep enough, or find the right rolling partner,
        you will observe some of the weirdest submissions you'll ever see - some with questionable origins, and others with 
        a history that is completely unknown.
    </p>
    <hr>
  
    <h3>The Scorpion Rib Crunch</h3>
    <p>
        The Scorpion Rib Crunch is a body-lock submission that applies pressure and crushes the ribs of your opponent. According
        to the instructor, it stems from a student of Pedro Sauer. Sauer is a BJJ legend, having trained under Rickson Gracie
        himself, and earning the "8th degree Coral belt" as well as being voted the "Best BJJ Instructor" in the world by an online
        poll in 2005. As for the technique itself, it is simply unorthodox. There aren't many submissions that target
        the body itself. This one applies pressure on the tips of the ribs using one's knees, and crushes them.
    </p>
    <br>
