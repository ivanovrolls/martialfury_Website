    <h3>The Sambo Forearm Slicer</h3>
    <p>
        As I wrote in my <a href = "../history_of_sambo.html">article on Sambo</a>, Sambo is a soviet martial art that has incorporated
        techniques from predominantly folk, wrestling styles from multiple countries. One of its techniques (though its origins seem
        obscured) is the Forearm Slicer. This submission, applies pressure on the ulna and radius bones which form your forearm. It
        certainly lives up to its name, as it can cause a complete fracture with displacement of these bones.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/sambo_forearm_slicer.gif" height = "auto" width = "70%">
    </div>
    <hr>
    <p>
      I personally suffered from
      this injury, which required me to undergo surgery and the installation and removal of rods to straighten my arm.
      If you wanted to make this submission particularly nasty and painful, you could use your supporting leg to push your other 
      leg's shin bone from behind, applying even more pressure.
    </p>
    <br>
    <h3>The Figure 8 Arm/Wrist Lock</h3>
    <p>
       This submission was taught to me in a traditional jiu-jitsu class back at home. I imagined that it was well known,
       but after researching it online, I couldn't find a single piece of documentation for it. As a result, I was forced
       to apply it on a willing participant and snap a picture of it.
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/figure_8_lock_1.jpg" height = "auto" width = "65%">
    </div>
    <hr>
    <p>
      As you can see, this is a variation of the well known armlock, except it applies pressure on the wrist as well as the shoulder.
      This submission can be used to break the wrist and dislocate the shoulder simultaneously.
    </p>
