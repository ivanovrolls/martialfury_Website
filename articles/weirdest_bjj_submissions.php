    </div>
    <p>
      I would be careful with this one! There are cases of people vomiting when it is applied on them, according to the
      demonstrator in the video.
    </p>
    <br>
    <h3>The Boston Crab Hold</h3>
    <p>
        The Boston Crab is a technique that was seen used as a submission in an MMA match by fighter Jonno Mears at 
        FCC (Full Contact Contender) 19. This move is also famously known as The Walls of Jericho, the special move of
        the pro wrestler Chris Jericho, currently signed with All Elite Wrestling. 
    </p>
    <hr>
    <div class = "centre_image">
        <img src = "../images/boston_crab_jonno.jpg" height = "auto" width = "100%">
    </div>
    <hr>
    <p>
      The reason why this submission is so bizarre,
      is due to the fact that it is mostly used and known from professional wrestling.
      Although many doubt the real-life applications of pro-wrestling,
      much of its techniques are legitimate and can (at times) be used in BJJ. Keep in mind however,
      that spinal locks (such as this one) are illegal in most BJJ tournaments unless they are part of a choke.
  </p>
