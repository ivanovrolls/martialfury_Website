      <h2>Knuckle and Bone Conditioning in different Martial Arts</h2>
      <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
      <hr>
        <p>
            Knuckle conditioning has been around for a long time and has been used for a plethora of
            purposes. In styles of Kung Fu, knuckle conditioning was known as Iron Palm or Iron Fist. This training 
            allows fighters to use their techniques with immense power, their hardened knuckles and hands being
            able to take vast amounts of punishment compared to that of the average person.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/iron_palm_training.jpg" height = "auto" width = "80%">
        </div>
        <hr>
        <p>
            This type of conditioning has been seen in many fighting styles, such as Muay Thai, Karate and even traditional
            bare-knuckle boxing. This conditioning aims to harden the bones of the hand to allow the utilization
            of techniques that couldn’t be properly applied otherwise, as well as minimizing the damage a fighter might suffer
            from punching with incorrect technique or striking harder areas of their opponent’s body. Common injuries include
            the boxer’s fracture. This is a fracture of the fifth metacarpal of your hand caused by striking
            with pinky and ring finger knuckles.
        </p>    
        <hr>
        <p>
            Knuckle conditioning comes in two forms: conditioning the knuckles through the healing of micro-fractures,
            and the callousing of the skin around the knuckles themselves which
            prevents skin being torn. Training methods for this vary depending on the martial art, as different cultures had
            different ideas and equipment available to them. The process of conditioning the bones usually takes years, so
            it’s important to be patient with this training.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/makiwara_training.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            The most common method is a piece of equipment called a Makiwara, thought to originate from Okinawa.
            It was developed for the practitioners of Okinawan
            Karate. The Makiwara is a striking post, which is secured to the ground, usually wrapped with thick rope to
            outline the striking area. Although it is primarily used for other purposes (such as the development of
            focus and technique) it can also serve to callous of the knuckles. The repetitive striking 
            of the post builds up knuckle damage over time, which when healed, will make the knuckles stronger. 
        </p>
        <hr>
        <p>
            In contrast, Muay Thai fighters usually focus their conditioning on the shins, to ensure their
            legs are rigid and can take the punishment of bone-on-bone contact. These
            fighters are notorious for their incredible kicking power. Much of their conditioning comes from numerous 
            exercises – the most impressive of which consists of repeatedly
            kicking their local banana trees due to their soft and rubber-like wood. But don’t get the wrong idea, kicking
            these is still a pain for the inexperienced.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/banana_trees.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Furthermore, <a href="https://www.livestrong.com/article/422823-jumping-rope-to-strengthen-shins/">this article at livestrong.com</a>
            states that rope-skipping and jogging can be used for conditioning 
            the shin bones too. Though it is worth noting that the repeated stress of bouncing on your legs can
            agonizing shin splints. On the other hand, these aren’t very good
            exercises for conditioning if they are not paired with other methods.
        </p>
        <hr>
        <p>
            Bare knuckle boxers also used to train their bodies to avoid injuries. However, their
            focus differed from that of the styles above. They emphasised on the hardening of their skin,
            and prioritised protecting their face from cuts rather than their hands from injuries. Instead
            of hardening their knuckles to be able to punch the skulls of their opponents, they adapted by targeting
            their body, which explains their memorable fighting stance.
            In fact, many boxers used their head as a weapon and took advantage of this 
            – they would purposefully aim to get their forehead in the way of their opponent’s fist in an attempt to break it upon impact.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/trad_boxing_stance.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            The conditioning of the skin was done mainly through two means: the first was menial work which resulted in
            the callousing of your hands. The second was done through the smearing of different liquids and concoctions.
            <a href="http://www.boxing.com/there_will_be_blood.html#:~:text=That%20great%20ringside%20doctor%20of,to%20harden%20their%20facial%20skin.">
            A summary of some bare knuckle boxing history in the article above</a>,
            claims that this was done through either the use of brine, or a solution of water and rock salt.
        </p>
        <hr>
        <p>
            In more traditional martial arts, conditioning was used for the employment of techniques that could not be used
            otherwise without breaking fingers, knuckles, wrists, or hands. Jesse Enkamp, a passionate martial artist and karate
            champion, who founded the Karate Nerds community, gives perfect examples of such techniques (which are present in
            Karate but also other arts such as Taekwondo) in his video below.
        </p>
        <hr>
        <div class="iframe-container">
            <iframe src="https://www.youtube.com/embed/TcJ-fYjCx44" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <p>
            Although bone conditioning was quite normal for martial artists in the past, it’s relatively uncommon these days as
            it takes too long to reap any benefits, as well as involving a lot of risk. It is very
            rare to find successful and legal combat sports that do not require the use of gloves or other protective gear in
            this day and age, so knuckle conditioning is useless. 
            In fact, the BKFC (Bare Knuckle Fighting Championship) is the first promotion allowed to hold a
            sanctioned bare-knuckle event in the United States since 1889.
        </p>
