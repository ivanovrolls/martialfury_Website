    <h2>Supplementing martial arts with strength training</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
            I recently competed at my first BJJ tournament and I had an amazing experience. It was thrilling to be on the
            mats, and it was a great experience to hang out with my teammates. This competition was the Brighton BJJ 247 Open,
            on the 19th of July. There were a lot of things that I think would be helpful to share with you. I have embedded my 
            match below.
        </p>
        <br>
        <div class="iframe-container">    
          <iframe src="https://www.youtube.com/embed/g4xc8GIxfRA" title="Brighton BJJ Open 247 First Match - 19062022" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
          <p style = "color: grey; text-align: center; font-size: 15px;">I am wearing the black gi with the red belt</p>
        </div>
        <br>
        <p>
            The event was held at a hotel, and I was expected to arrive at least an hour early. All notifications were sent to me
            via text message and email, but I was expected to keep track of my match start time using <a href = "https://www.smoothcomp.com">
            Smoothcomp</a>. Remember, that match times are dynamic, and can change at any time - this is why
            I arrived 2 hours early to ensure enough time to weigh in, warm up, and familiarise myself with the environemnt.
        </p>
        <hr>
        <p>
            I promptly put on my gi and weighed in at 80.4kg. My match was moved to an earlier time by 30-45 minutes
            from its original time, so I was glad to have given myself extra time. After wrapping up my fingers in tape, 
            I waited for a bit, and then went to warm up 15 minutes before my match for about 5-10 minutes.
        </p>
        <hr>
        <p>
            I had been repeatedly warned by my professor in my academy in Scotland, and the one in my gym in the south, that
            warming up was extremely important - not warming up enough is one of the most common mistakes made by those in their 
            first competitions. The advice given to me, was to work up a good sweat. I had been given a
            routine to do so, which was of great help - speak with your coach for this.
        </p>
        <br>
        <div class = "centre_image">
            <img src = "../images/second_comp_grlla.jpg" height = "auto" width = "65%">
            <p style = "color: grey; text-align: center; font-size: 15px;">This picture is actually from my second competition, the London IBJJ Internationals Open</p>
        </div>
        <br>
        <p>
            Mine consisted of doing 5 sprawls, 10 pushups, and 15 squats on loop for 5 minutes. I stopped mine early however,
            and finished off with some explosive pushups and jumping jacks as I began to panic that I was pressed for time. 
            Nevertheless, I had gotten my blood flowing adequately and felt ready to go.
        </p>
        <br>
        <p>
            As I waited for my match to begin, I watched those on the mat before me. Due to my previous experience in competition,
            with 7 years of competitive swimming experience, I was used to doing a "power pose" before my events. Although it is
            not a heavily researched technique, I felt that it helped to calm my nerves in the moment, and when it was time for me 
            to step on the mats and shake hands, I was unexpectedly calm.
        </p>
        <hr>
        <p>
            As soon as the match started, I felt the adrenaline dump hit me and almost immediately, I was out of breath. My mind 
            would not register a single thought, and my body was on autopilot; everything was happening extremely quickly and my muscles
            exhausted themselves in ways that simply could not be replicated during normal class rolling. My forearms were extremely sore 
            from fighting for grips, and I could barely use my hands by the end of it all.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/bjj_black_belt_comp.jpg" height = "auto" width = "100%">
        </div>
        <hr>    
        <p>
           Multiple times throughout the match, my opponent and I were made to reset. Those resets were the few moments where I was able
           to make an attempt at catching my breath. Even during these short breaks, my mind could not form a coherent thought. It was
           similar to the feeling I had when sparring during my striking sports, where everything is instinct and my head is empty and
           clear. My skills were dulled down extremely, and I made many mistakes that I don't usually make in training. The worst was 
           not cutting an angle on my triangle choke. Although I have no issue finishing the choke in training, I spent two whole
           minutes of the round attempting to apply it whilst I was squared up to my opponent. 
        </p>
        <hr>
        <p>
            I won the match, but it was not until the last 20 seconds of it that I realized that I was leading up on points.
            I believed that I was losing and was frantically battling for the submission, when I got a brief look at the score board.
            At the end of the match, my mouth was so dry that I was gagging on my mouth guard. I was in delirium, and shocked
            to have my hand raised in victory by the referee. The purple belt coaching me, helped to prop me on the wall in a
            recovery position so I could get my breath back.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/second_comp_win.jpg" height = "auto" width = "100%">
            <p style = "color: grey; text-align: center; font-size: 15px;">This picture is also from my second competition, after winning my first match by submission</p>
        </div>
        <hr>
        <p>
            Soon enough, it was time for my second match. Unfortunately, I was submitted almost immediately by guillotine, after rushing to take my opponent's back, and
            him suddenly turning around and sinking it in. I was told by a more experienced friend of mine, that I had cooled down too much after my first match,
            and I had made a mistake by not warming up. Furthermore, it took me a solid 10 minutes to recover my breath, perhaps pointing out an issue with my cardio.
        </p>
        <hr>
        <p>
           In comparison to my first competition, my second one went much smoother. The differences in my composure in the first matches of both comps was drastic.
           I was much more cool, and more aware of what was going on. I was able to rest in different positions rather than continously exploding into my next move,
           allowing me to maintain a steady pace. I made less mistakes, and was much more in control of myself - I wasn't simply on autopilot. All in all,
           experience definitely seemed to matter.
        </p>
        <hr>
           I urge you all to at least try one competition, and see if it's for you. Regardless of your expectations for yourself, it will be a
           positive experience, and as long as you leave everything you have on the mat, you will go home feeling like a king. If you are for some
           reason, disappointed in yourself after your performance, which you shouldn't be (especially in your first couple of competitions), 
           just look forward to your next competition. Each match is hours of worth of training, and you will improve immensely. Good luck!
