    <h2>The Mind-Body Connection</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
          Over my years of training I became fascinated by the connection between the
          mind and the body. I have heard many people, claim that those who focus too much on their
          physical well-being, end up overlooking their mental health or fortitude. There are also many philosophers,
          who have attempted to rationalise the connection between our mind and the body. In this article, I aim to deconstruct
          the impact of physical fitness, diet, and the way that we treat our body overall, on the mind and one's image of themselves.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/plato_statue.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
          Physical exercise is an action that involves partaking in exhausting activities (sometimes
          dangerous ones too) for the ultimate purpose of achieving self-improvement. The action of doing this, by nature,
          places stress on your mind as well as your body. Some argue that physical exercise cannot strengthen the mind
          because the adoption of fitness regimens and sports is commonly done by those
          with deep rooted issues and insecurities, and that those issues will remain regardles of one's physical stature.
          I argue, that this is only true to a certain extent.
        </p>
        <hr>
        <p>
          There are many times where your mind screams at you to stop the exercise you're doing because you are supposedly
          "at your limit". This leads to mental strengthening, since your subconscious observes that the
          body is more capable than it originally thought: you push through it for those extra reps. 
        </p>
        <hr>
        <p>
          Every rep that you do past your perceived limitation, is like sticking a middle-finger to the voice in your head
          that says "you cannot succeed". Every time you push through, you prove that part of mind incorrect; when you keep doing
          this, "you cannot succeed" becomes "fucking do it".
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/strength.jpg" height = "auto" width = "100%">
        </div>
        <hr>        
        <p>
          Pushing through to this stage is what takes you to the next level, and what emboldens your mind and spirit
          to believe you are better and stronger than you were before. This newfound strength within yourself is why 
          <a href = "https://www.ncbi.nlm.nih.gov/pmc/articles/PMC5068479/">your self-esteem and confidence increases.</a>
          It is true that many people sink themselves into fitness and exercise due to insecurities and low self-esteem. But this is true
          for any hobby.
        </p>
        <hr>
        <p>
          Regardless, it is important to consider the idea that physical fitness is one of the best coping mechanisms for such issues. 
          Many insecurities stem from feeling inadequate about our bodies, and these perceived imperfections can be eroded over time
          by adopting a lifestyle that promotes health and strength. The power of the human body doesn't stop at its infinite adaptibility.
          It extends to the psychological impact it has too, and it is neccesary that we begin to recognise this.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/insecurity.png" height = "auto" width = "100%">
        </div>
        <hr>        <p>
          Many modern and progressive (these tend to be pre-dominantly western) societies push the ideology of 
          being "perfect the way you are", but that ends up encouraging people to accept fixable flaws
          about themselves instead of attempting to correct them. As it stands, we are facing an obesity epidemic, and some of the highest levels ever recorded
          of depression, anxiety and overall bad mental health amongst the population, specifically amongst the younger generations. If exercise
          were more encouraged, it would certainly lead to an improvement of all these factors stated.
        </p>
        <hr>
        <p>
          Not only is fitness a driving factor for one's mental health, diet is too. Obesity (measured by BMI) has been on a very steady
          increase ever since the 1970-80s, according to the graph below, taken from the <a href = "https://www.niddk.nih.gov/health-information/health-statistics/overweight-obesity">
          National Institue of Diabetes and Digestive and Kidney Diseases</a>. Coincidentally, this is the same time period that many
          ultra-processed foods were rolled out in the USA, such as Chicken McNuggets, I Can't Believe It's Not Butter, Pudding Pops
          and more. This is the same period where McDonald's and other similar fast food chains, became mainstream
          in American society.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/obesity_rate_usa.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
          The issues are only becoming worse over time - there is no sign of stoppage or slowing. Suicide rates are rising too.
          But it's not like we are reaching a tipping point, and it's not like the mega-corporations or governments profitting
          off of this would care if we were. It is crucial that we begin to take better care of ourselves and that we start
          treating our bodies the way they should be. People will continue to die and feel lesser than they are until we begin
          to move past this era. 
        </p>
        <hr>
        <p>
          The era of hyper-processed chemicals being passed as food to appeal to the new
          trending diets proposed by fitness "experts", many of which profit off of vulnerable people attempting to better themselves.
          The era in which people have become so comfortable and so passive that they don't even need to walk to a store, let
          alone hunt, for their food - everything is now delivered to you by the click of a button.
          The era in which everything is treated with pills, and medicine focuses on the symptoms rather than the cause
          of these problems.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/chemical_shit_storm.jpg" height = "auto" width = "75%">
        </div>
        <hr>        <p>
          I hope this article will help with this. There's nothing wrong with occasional indulgence , but
          your diet shouldn't mainly consist of food that is heavily altered from its original state e.g. donuts,
          crisps packets, chocolate bars, and cheap meat cooked by questionable establishments. Focus on consuming
          naturally ocurring foods, and do your best to cook them yourself so you can be more certain of the process
          the food has undergone. Focus on meat, eggs and vegetables.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/beef_broccoli.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
          Get to the gym and lift. Go outside and jog. Return home and stretch. Eat well. Create positive habits
          around your body, and follow a lifestyle that is best for your health. Your happiness will show itself soon.
          Your subconscious is watching you at all times - it is watching to see whether you respect your body, and
          whether you stick to your promises. Your subconscious image of yourself directly stems from the way you 
          consciously treat your conscious self - do you eat real food? Do you abide by the promises you make to yourself?
          Do you criticise yourself as much as you should?
        </p>

