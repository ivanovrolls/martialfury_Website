      <h2>Throwing a Hook: Palm in or palm down?</h2>
      <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
      <hr>
        <p>
            This debate has been raging on for as long as I can remember. Everybody has their own personal preference, and many claim that the direction the palm faces is
            completely dependent on where your target is. Some call the palm in hooks the "American hook", and the palm down hook, the "European hook".
        </p>
        <hr>
        <p>
            Recently, I saw a video by <a href="https://www.youtube.com/user/FightTipsVideos">fightTips</a>, an amazing YouTube channel, in which they collaborated with the great, Roy Jones Jr. In this video, Team Roy (comprising of the boxer himself and his coach) go over why they personally train to use the palm
            in variation of the hook. What I found quite interesting, was that they had some well-versed professionals step in and explain that, not only is the palm in
            variation of the hook <b>better for generating power, but it also places less strain on the joints and minimises the dangers of rotator-cuff injuries.</b>
        </p>
        <br>
        <div class="iframe-container">    
            <iframe src="https://www.youtube.com/embed/sBrHz_hSJAY" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <br>
        <p>
            Their points on kinetic chains was particularly compelling. 
        </p>
        <hr>
        <p>
            <i>Kinetic Chains: a mechanical system by which athletes perform the majority of their movements in a large variety of sports, particularly the movements which require the generation of power e.g. baseball bat swing</i>
        </p>
        <hr>
        <p></p> 
            In fact, breakage of these kinetic chains, <b>leads to decreased performance</b> in tennis players, or even <b>injuries in 
            their shoulders or wrists</b>, regardless of their age. These disruptions in the kinetic chains are usually caused by:
        </p>
        <ol>
            <li>Muscle weaknesses or imbalances</li>
            <li>Lack of muscle flexibility</li>
            <li>Incompletely healed, or past rehabilitated injuries</li>
            <li>Joint stiffness or improper mechanics</li>
        </ol>
        <p>
            An effective kinetic chain, is characterised by two key components:
        </p>
        <ol>
            <li>Optimised flexibility and muscle strength</li>
            <li>Optimised use of mechanics and technique</li>
        </ol>
        <p>
            Although the phrase "kinetic chains" is uncommon in everyday language, anyone versed in 
            any martial arts training, is experienced in their use too. Every sport is based upon 
            optimising how you use your body, to increase speed, power, strength or any attributes that
            the sport is based on or relies upon.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/left_hook_home.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            However, there are numerous points that have been made explaining why the hook should be thrown with the palm facing the ground. It is argued that the activation of the biceps
            in the hook punch can be detrimental, because it leads to reduced relaxation in the arm, and can slow down your punch. Moreover, using the American hook can cause you to hit with all of your knuckles,
            instead of just your lead knuckles, resulting in metacarpal fractures. This point is especially important when throwing punches without hand protection such as gloves and wraps.
        </p>
        <hr>
        <p>
            Is this such a bad thing? It really depends on the context. Traditionally, in almost every martial art (and possibly in old-school, bareknuckle boxing) you
            were taught to hit with the knuckles of your index and middle finger. There is two leading reasons for this:
        </p>
        <ol>
            <li>Hitting with a smaller surface area leads to more damage done as Pressure = Force / Area</li>
            <li>Aiming to hit with your front two knuckles, aligns your forearm in a more stable manner</li>
        </ol>
        <p>
            Taking all of these points into account, I would argue it is important that beginners learn to use the European hook first (palm down).
            This will ensure that you learn how to throw hook punches correctly, as it will be harder to generate power without twisting your hips.
            The American hook allows for moderate power, without optimal hip rotation, which can lead to bad habits and reliance on arm strength
            instead of good technique for power punches.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/my_hook.jpg" height = "auto" width = "80%">
        </div>
        <hr>
        <p>
          I firmly believe, that both variations should be utilised. The reason for this, is that the Europen hook is better for longer range
          exchanges, and the American hooks is preferable for in-fighting. Because the American hook doesn't twist the arm inwards,
          it is much faster to unload at close ranges than the European hook, and also keeps the elbow closer to the body for protection.
        </p>
        <hr>
        <p>
          Nevertheless, the American hook leads to poor wrist structure when throwing it range, as it results in caving the wrist towards you to maintain the palm inwards.
          In fact, an American hook thrown at long range usually results in hitting your opponent with the inside of your wrist, which is called an open-glove punch and <b>is not recognised
          in amateur boxing</b>. This is where the European hook excels, enabling safer structure when throwing hooks at range, protection you from self-inflicted wrist and metacrapal damage, and 
          also extending your optimal distance for hook punches.
        </p>
        <hr>
        <p>
            In short, both hooks have their pros and cons, and many lean towards one or the other as boxers tend to have preference 
            for inboxing or outboxing, usually definied by their reach.
        </p>
