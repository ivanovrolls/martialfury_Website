      <h2>The Carnivore Diet</h2>
      <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
      <hr>
        <p>
            The Carnivore Diet is one that is surrounded by much controversy, mainly due to it going against
            decades and generations of conventional wisdom. It consists entirely of meat, usually beef, and
            animal products, such as eggs, milk and cheese.
        </p>
        <br>
        <img src="../images/meat.jpg" height="auto" width="100%">
        <br>
        <p>
            The diet excludes anything that is not a naturally ocurring product that originates from animals. 
            Everything else is abandoned – vegetables, fruit, nuts etc. The diet is based on the theory that ancestral
            populations, had diets that were primarily focused on fish and meat due to a lack of technological advancements in agriculture. 
        </p>
        <hr>
        <p>
            According to Healthline, the diet was first proposed by former American orthopaedic doctor, Shawn Baker who cites anecdotal 
            testimonials in his book from the diet’s followers as proof that the diet has been successful in
            curing cases of anxiety, arthritis, obesity, and other such ailments. Healthline further states, Shawn Baker’s <i>“medical licence was revoked by the New Mexico Medical Board
              due to concerns about his competency”</i>.
        </p>
        <hr>
        <p> Another famous proponent for this diet is Professor Jordan
            Peterson, a psychologist and author based in Canada: <i>“I eat beef and salt and water. 
            That’s it, and I never cheat. Ever. Not even a little bit”</i>. His aversion to other foods is due to his history
            of poor mental health, claiming that this diet helps him more than any other in maintaining stable health. This diet
            was also proposed to him by his daughter, a fitness blogger and diet expert, who too has suffered from depression,
            having inherited it from her father. 
        </p>
        <br>
        <img src="../images/jordan_peterson.jpg" height="auto" width="100%">
        <br>
        <p>
            Critics argue that this diet, regardless of its efficacy, would be extremely hard to stay dedicated to. They argue
            that it would be too expensive to exclusively eat (supplemented with other foods), and that one would get exhausted
            of eating such a limited amount of food for extended periods of time.
        </p>
        <hr>
        <p>
            However, this is not to say there is not any legitimacy to the ex-Doctor’s claims, although it would be possible to claim bias or falsification of results. Baker’s Instagram consists
            of him documenting his progress as he strictly follows his very own diet. The changes in physique are astounding.
        </p>
        <hr>
        <div class = "centre_image">
          <img src = "../images/shawn_baker.jpg" height = "auto" width = "65%">
        </div>
        <hr>
        <p>
            Arguably, one of the biggest reasons this diet is so controversial is because it spits in the face of education
            and commond knowledge, such as the supposed importance of a balanced diet.
            These ideas have been accepted for decades, until Baker claimed that animal foods
            contain everything your body needs for good health. In his book, he also cites anecdotal cases of this diet helping
            people recover from all sorts of injuries, from knees requiring surgery to even various forms of cancer.
        </p>
        <hr>
        <p>
            An article by Onnit, covering the topic, 
            quoted a certain Brian St. Pierre (Director of Performance Nutrition at
            Precision Nutrition) claiming that <i>“we need fat, and vitamins and minerals in certain amounts”</i> and that <i>“meat, specifically, can cover those needs”</i>. He went on to say <i>“that doesn’t mean
            that we shouldn’t eat plants but … it isn’t vital that we do"</i>.
        </p>
        <br>
        <div class = "centre_image">
          <img src = "../images/shbaker_meat.jpg" height = "auto" width = "100%">
        </div>
        <br>
        <p>
            Regardless of Baker’s drastic aesthetic changes, he has admitted that he has not had his state of health officially backed by any doctors and even announced
            that he had not had any blood checks done, during his interview on Joe Rogan’s podcast. However, Joe Rogan did try the carnivore diet himself. There’s a video about his experience with it, claiming
            he <b>lost ~7-12lbs just two weeks into the diet</b>. He also claimed that he felt more energetic, as he did not experience any crashing or mental cloudiness, although he did say it can cause explosive
            diarrhoea due to the lack of fibre in his diet. It is important to remember that he did take a lot of supplements whilst on this diet such as fish oils and vitamins tablets.
        </p>
        <br>
        <div class="iframe-container">
            <iframe src="https://www.youtube.com/embed/ivMoNhjnhqw" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
        </div>
        <br>
        <p>
            Since the interview, Baker did publish his bloodwork, which was quite controversial. For starters, he had a low level
            of testosterone, which is usual among men of his age (in his 50s), although he argues that it is because of the high amounts
            of carnitine in his diet. Carnitine is an amino acid found in meat which transports fats to mitochondrion.
        </p>
        <hr>
        <p>
            You can view more information, and an analysis on Baker's blood work <a href = "https://www.stopbeingconfusedabouthealth.com/shawn-bakers-bloodwork-carnivore-diet-explained-full/">here</a> and <a href = "https://robbwolf.com/2018/03/13/episode-385-dr-shawn-baker-carnivore-diet-and-dr-bakers-blood-work/">here</a>,
            as well as more resources online. Remember that, many proponents of this diet, such as Joe Rogan, take supplements such as fish oils
            and vitamin pills. It is also very important to eat fats in the meats you eat to avoid Rabbit Starvation, a case in which Arctic explorers
            seemingly died of starvation due to exclusively eating rabbit meat. As for the claim
        </p>
        <hr>
        <p>
          I will conclude by stating that there are many side effects you should consider when attempting this diet -
          having attempted it myself, at one point I drank over 4 litres of water in an hour, presumably
          due to a low electrolyte level (a pinch of salt in the water you drink). Please consider all of these
          factors, should you decide to attempt this diet. 
        </p>
