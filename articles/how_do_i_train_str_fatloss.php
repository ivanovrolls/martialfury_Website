    <h2>How do I train? Strength and Fat Loss</h2>
    <div class="sharethis-inline-share-buttons" style = "text-align: justify;"></div>
    <hr>
        <p>
            Everyone has different methods of training – these methods depend on the goals, circumstances, information, and
            resources available to the individual, time and a plethora of other factors. In this article, I will be outlining
            my training methods and diets throughout my recent journey.
        </p>
        <hr>
        <p>
            During the last couple of weeks, I have had an enormous surge in free time. This is due to my academic year having
            finished; I took this as an opportunity to hunt for my body’s next level. I had two aims: to gain strength, but
            mainly, to lose fat. This is because I have only been bulking for the past 3-4 years, without putting in any real
            efforts into maintaining a shredded physique. I didn’t look fat, I actually managed to stay slim in terms of fat, but
            I did start putting out a belly over the last 6-9 months or so.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/weightlifters.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            The first thing I established was that it was imperative for me to set a limit for my calories – I firmly believe that
            it is impossible to lose mass without being in a caloric deficit. This official study outlines, that
            <a href = "https://pubmed.ncbi.nlm.nih.gov/18025815/ ">regardless of methods, energy deficits alone are responsible
            for weight loss.</a>
        </p>
        <hr>
        <p>
            This led me to my next dietary conclusion: I was free to eat whatever I chose, as long as I abided by my
            caloric goal. And I did! There were many days where I chose to eat chocolate, pizza, treats and other foods that are
            generally considered unhealthy. Some of these foods were packed with calories, so in order to allow myself to satisfy
            my cravings, I would plan out these meals in advance. If a single pizza was enough to fulfill my caloric intake for
            the day, I would fast for the day up until dinner, and enjoy my pizza.
        </p>
        <hr>
        <p>
            The only macro-nutrient I was worried about was my protein – during a caloric deficit, in order to avoid losing
            muscle mass, it is extremely important to maintain high protein intake. I didn’t explicitly track it, but I ate
            high calorie foods and meats almost daily. I would indubitably recommend mixing whole milk with whey-based protein
            powder to make some shakes.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/pizza.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Depending on the individual, the caloric deficit necessary will vary – <a href = "https://www.acefitness.org/education-and-resources/lifestyle/blog/2461/do-men-and-women-have-different-nutritional-needs/">
            women for example require less calories than men daily.</a> Personally, at the time of this diet, I was a 19 year
            old man at about 178cm tall, weighing in at 82-83kg. I set my primary calorie goal to 1600 daily, and a secondary
            goal at 2100. I usually consume around 2500-3000 daily, so it seemed like cutting my intake to around half would
            be a good target. 
        </p>
        <hr>
        <p>
            Many fitness enthusiasts would agree that cardio is important for cutting weight. Cardiovascular training is the
             most efficient for burning calories, with 
             <a href = " https://www.healthline.com/health/what-exercise-burns-the-most-calories">running being the best for most
                calories burnt per hour.</a>
        </p>
        <hr>
        <p>
            However, as someone who did competitive swimming for 7 years, I guarantee that it is a much better exercise for
            cardio. Why? The reason isn’t calorie based – depending on the resources you examine; you will find multiple
            conclusions on running versus swimming on energy expenditure. Regardless of the reality, swimming is a much safer
            sport for the joints as there is no repetitive impact on the knees or ankles. Running daily can lead to several
            joint complications, whereas swimming daily is perfectly okay.
        </p>
        <hr>
        <p>
            My personal method? Sprints. I will usually do 20 back-to-back rounds of 10 seconds sprinting and a 6 second
            light jog. I love this method as it is the least time consuming. Unfortunately, due to some knee injuries (don’t
            run daily, trust me) I was unable to do roadwork. So how did I cope? BJJ rolling of course! I attended BJJ
            classes at least four times a week and each of these classes involved a minimum of three 5-6 minute rounds of
            rolling. Moreover, I attended 1.5 hour open gym boxing sessions quite often, if I had the money, which I spent
            perfecting techniques on heavy bags.
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/run_vs_swim.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            This was more than enough to take care of my martial arts and cardio training, especially coupled with some drills
            and technique practice at home. So finally, what about resistance training? 
        </p>
        <hr>
        <p>
            For six days weekly, Monday to Saturday, I trained morning and evening. Sunday was the day of rest. Each day was
            dedicated to a specific muscle group, with both workouts being dedicated to hitting the group in divergent methods.
            I also aimed to have two days per week dedicated for each muscle group, although this wasn’t always possible.
        </p>
        <hr>
        <p>
            I would always train 5-7 sets of each exercise I chose to do, with enough weight to handle the 3-5 rep range. I
            would also super set exercises for the same muscle group quite often. This was supposed to increase my strength.
            This extensive training combined with my daily 7g intake of Creatine, allowed me to work wonders (results in the
            conclusion). Here is an example of my chest workouts:
        </p>
        <hr>
        <p>
            Morning:
        </p>
        <ul>
            <li>5-7 sets Bench Press, 3-5 reps</li>
            <li>5-7 sets Pinch Press, 3-5 reps <b>Superset with: DB Fly</b></li>
            <li>1 set of Push Ups until failure</li>
        </ul>
        <p>Evening:</p>
        <ul>
            <li>5-7 sets Cable Fly, 3-5 reps</li>
            <li>5-7 sets of Incline Press, 3-5 reps</li>
            <li>3 supersets: Pushups and Dips</li>
        </ul>
        <hr>
        <p>
            It was common for me to combine weighted resistance exercises with bodyweight exercises in all of my workouts. This
            was to keep my body guessing as to what was coming – the human body is the pinnacle of evolution. It is the perfect
            adaptation machine: every time your body becomes stronger, sheds fat, packs on or loses muscles, it is adapting to
            suit its specific conditions. Your job is to make sure you adapt your body for your conditions. Although, it is
            important to understand when and when it won’t adapt. If you train your chest the exact same way every time, then
            the body won’t adapt, and it won’t challenge itself.
        </p>
        <hr>
        <p>
            Thus, I made a list of exercises that I was most proficient with, or enjoyed most for each muscle group, and rotated
            them with each workout. Lastly, I would like to clarify the reason I trained twice a day. 
        </p>
        <hr>
        <p>
            The idea was first given to me after a friend of my father’s outlined how he had heard gymnasts trained – twice a
            day at 60-85% intensity. I delved in a little deeper and found that old-school Bulgarian bodybuilders and strongmen
            would<a href = "https://oldschool362.medium.com/how-ivan-abadjiev-transformed-bulgarian-weightlifting-5e515c82c96a"> train 3-4 times daily</a>,
            but with very little weight and resistance. 
        </p>
        <hr>
        <div class = "centre_image">
            <img src = "../images/IvanAbadjiev.jpg" height = "auto" width = "100%">
        </div>
        <hr>
        <p>
            Being a Bulgarian myself, I decided to take some inspiration from old wisdom. Due to me training at 100% intensity
            every workout, there were definitely some unplanned rest days in which I could barely get up out of my bed. I had
            overtrained.
        </p>
        <hr>
        <p>
            The results, however, were absolutely worthwhile, at least for me. My Bench Press moved from an 80kg one-rep max to
            90kg. I used to rep 3-5 at 110-115kg on my strict deadlift, and I increased this to 130kg. Moreover, I lost 2kg of
            fat. This was all in the span of 5-6 weeks. I hope that this information will serve you in your future training and
            dieting regimes. Stay strong, stay disciplined.
        </p>
    
